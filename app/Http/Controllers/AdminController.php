    {
        $users = User::with(['userGroups' => function ($query) {
            $query->join('groups', 'groups.id', '=', 'user_groups.group_id')->orderBy('organisation');
        }])->paginate(10);

        $courses = Course::orderBy('name')->paginate(10);
        $groups = Groups::orderBy('organisation')->paginate(10);

        return view('dashboard.admin.index', compact('courses', 'users', 'groups'));
    }

